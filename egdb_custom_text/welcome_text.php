Avocado (<i>Persea americana</i> Mill.) (2n = 24) is a member of the flowering plant family Lauraceae, which includes at least 2500 pantropical to temperate species, most of which are woody trees [13]. This family contains a few edible fruit trees, and includes cinnamon, bay laurel and sassafras. Avocado has been divided into three landraces: Mexican, Guatemalan and West Indian. Hass, a hybrid of the Guatemalan and Mexican races (without a known breeding history), is the principal cultivar, originating from a chance seedling that has been clonally propagated ever since. 
