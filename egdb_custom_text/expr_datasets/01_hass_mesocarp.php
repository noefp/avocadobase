<p style="text-align:justify">
  Avocado fruits were obtained from a commercial supplier, three independent fruits (biological replicates) were sampled at 150, 240, 300 and 390 days after fruit set (DAFS).
  Three biological replicates for each developmental stage. cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina HiSeq 2500 and normalized to TPM.
  This dataset was published by <a href="https://doi.org/10.1186/s12864-019-5486-7" target="_blank">Vergara-Pulgar et al. 2019 </a> and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA483022" target="_blank">PRJNA483022</a>.
</p>

